#include "glad.h"

#define BUFFER_SIZE 16384

static GLfloat   gUiTexBuff[BUFFER_SIZE * 8];
static GLfloat  gUiVertBuff[BUFFER_SIZE * 8];
static GLubyte gUiColorBuff[BUFFER_SIZE * 16];
static GLuint  gUiIndexBuff[BUFFER_SIZE * 6];
static unsigned int gUiBufferPosition = 0;

static GLuint gUiTexVBO = 0;
static GLuint gUiVertVBO = 0;
static GLuint gUiColorVBO = 0;
static GLuint gUiIndexIBO = 0;
static GLuint gUiVAO = 0;

static GLuint gUiShader = 0;
static GLuint gUITexture;
static GLint gUiTexAttrib = 0;
static GLint gUiVertAttrib = 0;
static GLint gUiColorAttrib = 0;
static GLint gUiMVPUniform = 0;
static GLint gUiTexUniform = 0;

void r_init(unsigned int screen_width, unsigned int screen_height) {
    gUiScreenWidth = screen_width;
    gUiScreenHeight = screen_height;
    /* init gl */
    glGenBuffers(1, &gUiTexVBO);
    glGenBuffers(1, &gUiVertVBO);
    glGenBuffers(1, &gUiColorVBO);
    glGenBuffers(1, &gUiIndexIBO);
    glGenVertexArrays(1, &gUiVAO);

    /* init texture */
    glGenTextures(1, &gUITexture);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, gUITexture);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, ATLAS_WIDTH, ATLAS_HEIGHT, 0,
        GL_RED, GL_UNSIGNED_BYTE, atlas_texture);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);

    gUiShader = glCreateProgram();

    static const char* v_source = "#version 330 core\n"
        "uniform mat4 mvp;\n"
        "in vec2 position;\n"
        "in vec2 texCoord;\n"
        "in vec4 color;\n\n"
        "out vec2 _texCoord;\n"
        "out vec4 _color;\n\n"
        "void main() {\n"
        "\t_texCoord = texCoord;\n"
        "\t_color.x = max(min(color.x / 255.0, 1.0), 0.0);\n"
        "\t_color.y = max(min(color.y / 255.0, 1.0), 0.0);\n"
        "\t_color.z = max(min(color.z / 255.0, 1.0), 0.0);\n"
        "\t_color.w = max(min(color.w / 255.0, 1.0), 0.0);\n"
        "\tgl_Position = mvp * vec4(position.x, position.y, 0.5, 1.0);\n"
        "}";

    static const char* f_source = "#version 330 core\n"
        "uniform sampler2D tex0;\n"
        "in vec2 _texCoord;\n"
        "in vec4 _color;\n\n"
        "out vec4 FragColor;\n\n"
        "void main() {\n"
        "\tvec4 t0 = _color;\n"
        "\tvec4 t1 = texture(tex0, _texCoord);\n"
        "\tFragColor = vec4(t0.r, t0.g, t0.b, t1.r);\n"
        "}";

    // Compile vertex shader
    GLuint v_shader = glCreateShader(GL_VERTEX_SHADER);
    glShaderSource(v_shader, 1, &v_source, NULL);
    glCompileShader(v_shader);
    int success = 0;
    glGetShaderiv(v_shader, GL_COMPILE_STATUS, &success);
    if (!success) {
        char infoLog[512];
        glGetShaderInfoLog(v_shader, 512, NULL, infoLog);
        glDeleteShader(v_shader);
        return;
    };

    // Compile fragment shader
    unsigned int f_shader = glCreateShader(GL_FRAGMENT_SHADER);
    glShaderSource(f_shader, 1, &f_source, NULL);
    glCompileShader(f_shader);
    success = 0;
    glGetShaderiv(f_shader, GL_COMPILE_STATUS, &success);
    if (!success) {
        char infoLog[512];
        glGetShaderInfoLog(f_shader, 512, NULL, infoLog);
        glDeleteShader(f_shader);
        return;
    };

    // Link shaders
    glAttachShader(gUiShader, v_shader);
    glAttachShader(gUiShader, f_shader);
    glLinkProgram(gUiShader);
    success = 0;
    glGetProgramiv(gUiShader, GL_LINK_STATUS, &success);
    if (!success) {
        char infoLog[512];
        glGetProgramInfoLog(gUiShader, 512, NULL, infoLog);
        glDeleteShader(v_shader);
        glDeleteShader(f_shader);
        return;
    }
    glDeleteShader(v_shader);
    glDeleteShader(f_shader);

    gUiMVPUniform = glGetUniformLocation(gUiShader, "mvp");
    gUiTexUniform = glGetUniformLocation(gUiShader, "tex0");
    gUiTexAttrib = glGetAttribLocation(gUiShader, "texCoord");
    gUiVertAttrib = glGetAttribLocation(gUiShader, "position");
    gUiColorAttrib = glGetAttribLocation(gUiShader, "color");

    glUseProgram(0);
}

void r_shutdown() {
    glDeleteTextures(1, &gUITexture);
    glDeleteProgram(gUiShader);
    glDeleteBuffers(1, &gUiTexVBO);
    glDeleteBuffers(1, &gUiVertVBO);
    glDeleteBuffers(1, &gUiColorVBO);
    glDeleteBuffers(1, &gUiIndexIBO);
    glDeleteVertexArrays(1, &gUiVAO);
}

